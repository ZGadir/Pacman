def main():
    global game_over
    while not game_over:
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                game_over = True
        screen.fill(BLACK)
        draw_board()
        pygame.display.update()
        timer.tick(FPS)

    pygame.quit()

if __name__ == "__main__":
    main()